def populate():
    conn = get_connection()
    cur = conn.cursor()
    cur.execute("""
        CREATE TABLE IF NOT EXISTS products (
            id INTEGER PRIMARY KEY,
            name TEXT,
            description TEXT,
            price REAL
        )
    """)
    for name, desc, price in records:
        cur.execute("INSERT INTO products (name, description, price) VALUES (?, ?, ?)", (name, desc, price))
    conn.commit()
    conn.close()
    print("Database created and populated successfully.")


